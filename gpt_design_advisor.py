import os
import openai
from dotenv import load_dotenv

load_dotenv()

# Получаем API ключ из переменных окружения
openai.api_key = os.getenv('OPENAI_API_KEY')

def get_design_advice():
    prompt = """
    Я создаю сайт для нутрициолога. Основные элементы:
    - Главная страница с информацией о специалисте
    - Страница "Обо мне" с фотографией, образованием и подходом к работе
    - Страница услуг с описанием консультаций
    - Страница контактов
    
    Текущий дизайн включает:
    - Плавающие элементы (фрукты и овощи) для декора
    - Волнистые формы для фотографий (border-radius: 30% 70% 70% 30% / 30% 30% 70% 70%)
    - Мягкие тени и градиенты
    - Цветовая схема: зеленые и натуральные тона
    
    Пожалуйста, дай 5 конкретных советов по улучшению дизайна, фокусируясь на:
    1. Современные тренды в веб-дизайне
    2. UX/UI лучшие практики
    3. Конверсия и привлечение клиентов
    4. Уникальность и запоминаемость
    5. Профессиональный вид
    
    Советы должны быть конкретными, с примерами реализации.
    """
    
    try:
        response = openai.ChatCompletion.create(
            model="gpt-4",
            messages=[
                {"role": "system", "content": "Ты опытный веб-дизайнер, специализирующийся на сайтах для специалистов в области здоровья и велнеса."},
                {"role": "user", "content": prompt}
            ]
        )
        return response.choices[0].message.content
    except Exception as e:
        return f"Ошибка при получении советов: {str(e)}"

if __name__ == "__main__":
    print("\nПолучаем советы по дизайну от ChatGPT...\n")
    advice = get_design_advice()
    print(advice)
